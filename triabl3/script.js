(function () {

  // ── Element refs ───────────────────────────────────────────
  const navbar         = document.getElementById('navbar');
  const heroStage      = document.querySelector('.hero-stage');
  const navWordmark    = document.querySelector('.nav-wordmark');
  const navTitles      = document.querySelectorAll('.nav-title');   // multiple links
  const hamburgerSpans = document.querySelectorAll('.hamburger span');
  // Note: .nav-logo (the Ajrakh SVG mark) stays maroon always — not lerped.
  const canvas         = document.getElementById('particle-canvas');
  const ctx            = canvas.getContext('2d');
  const heroWordmark   = document.getElementById('hero-wordmark');

  // ── Color endpoints ────────────────────────────────────────
  const BG_DEEP    = [61,  15,  26];    // #3d0f1a — vine red / deep burgundy
  const BG_LIGHT   = [245, 241, 232];   // #f5f1e8 — warm parchment
  const TEXT_LIGHT = [242, 236, 224];
  const TEXT_DARK  = [26,  26,  26];

  // ── Helpers ────────────────────────────────────────────────
  function lerp(a, b, t)    { return a + (b - a) * t; }
  function clamp(v, lo, hi) { return Math.max(lo, Math.min(hi, v)); }
  function easeOut(t)       { return t * (2 - t); }
  function easeIn(t)        { return t * t; }

  function lerpRgb(c1, c2, t) {
    return `rgb(${Math.round(lerp(c1[0],c2[0],t))}, ${
                 Math.round(lerp(c1[1],c2[1],t))}, ${
                 Math.round(lerp(c1[2],c2[2],t))})`;
  }

  // Canvas & particle state
  let W = 0, H = 0;
  let particles    = [];
  let canvasProgress = 0;   // 0 = scattered → 1 = formed  (driven by Phase 1 scroll)
  let time         = 0;
  const mouse      = { x: -9999, y: -9999 };

  function initCanvas() {
    W = canvas.offsetWidth  || window.innerWidth;
    H = canvas.offsetHeight || window.innerHeight;
    canvas.width  = W;
    canvas.height = H;
    buildParticles();
  }

  function buildParticles() {
    // ── Uniform-scale mapping (preserves India's portrait aspect ratio) ──
    // Without this, wide screens stretch India horizontally (squished look).
    // We map the reference image's 1024×1024 coord space into a SQUARE region
    // centered on the canvas, sized to ~90% of the canvas HEIGHT.
    const mapSize = Math.min(W, H) * 0.92;  // slightly taller on narrow screens
    const offX    = (W - mapSize) / 2;
    const offY    = (H - mapSize) / 2;

    particles = INDIA.map(([nx, ny]) => {
      // Edge softening: ±8px random jitter on targets
      // Prevents mechanical straight-line edges, adds organic feel
      const jitter = 8;
      return {
        // Target: India shape position in the centered square region
        tx: offX + nx * mapSize + (Math.random() - 0.5) * jitter,
        ty: offY + ny * mapSize + (Math.random() - 0.5) * jitter,
        // Base: random scatter across full canvas
        bx: Math.random() * W,
        by: Math.random() * H,
        // Size range widened slightly for more texture
        size:      1.2 + Math.random() * 2.6,    // 1.2–3.8px
        // Alpha range widened for depth — some bright, some dim
        baseAlpha: 0.35 + Math.random() * 0.58,  // 0.35–0.93
        // Floating oscillation (unique per particle)
        fPhase: Math.random() * Math.PI * 2,
        fFreq:  0.30 + Math.random() * 0.55,
        fAmp:   3.0  + Math.random() * 5.5,
      };
    });
  }

  // ── Draw loop ──────────────────────────────────────────────
  function drawFrame() {
    ctx.clearRect(0, 0, W, H);

    // Eased progress: fast pull at start, gentle settle at end
    const t = easeOut(canvasProgress);

    // Float amplitude shrinks as particles settle — still alive, less restless
    const floatScale = 1 - canvasProgress * 0.72;

    for (const p of particles) {
      // Floating offset (sinusoidal drift, unique per particle)
      const fx = Math.sin(time * p.fFreq + p.fPhase)        * p.fAmp * floatScale;
      const fy = Math.cos(time * p.fFreq * 0.75 + p.fPhase + 1.2) * p.fAmp * floatScale;

      // Interpolated position (base → target) + float
      let px = lerp(p.bx, p.tx, t) + fx;
      let py = lerp(p.by, p.ty, t) + fy;

      // Cursor repulsion — gentle push within 90px radius
      const dx   = px - mouse.x;
      const dy   = py - mouse.y;
      const dist2 = dx * dx + dy * dy;
      if (dist2 < 8100 && dist2 > 0.01) {   // 8100 = 90²
        const dist  = Math.sqrt(dist2);
        const force = (1 - dist / 90) * 8;
        px += (dx / dist) * force;
        py += (dy / dist) * force;
      }

      // Alpha: slightly brighter when organized
      const alpha = lerp(p.baseAlpha * 0.62, p.baseAlpha, canvasProgress);

      // Glow halo (cheap — no ctx.shadow) — larger, very faint circle
      ctx.beginPath();
      ctx.arc(px, py, p.size * 3.2, 0, Math.PI * 2);
      ctx.fillStyle = `rgba(215, 176, 112, ${alpha * 0.14})`;
      ctx.fill();

      // Core particle
      ctx.beginPath();
      ctx.arc(px, py, p.size, 0, Math.PI * 2);
      ctx.fillStyle = `rgba(215, 176, 112, ${alpha})`;
      ctx.fill();
    }
  }

  function tick() {
    time += 0.006;    // slow, meditative pace
    drawFrame();
    requestAnimationFrame(tick);
  }

  // ══════════════════════════════════════════════════════════
  //  SCROLL SYSTEM  (two-phase — unchanged from prior session)
  // ══════════════════════════════════════════════════════════

  function updateHeroExit(p2) {
    const scale   = lerp(1, 0.96, easeIn(p2)).toFixed(4);
    const opacity = lerp(1, 0, p2).toFixed(4);
    heroStage.style.transform = `scale(${scale})`;
    heroStage.style.opacity   = opacity;
  }

  function updateBackground(p2) {
    document.body.style.backgroundColor = lerpRgb(BG_DEEP, BG_LIGHT, p2);
  }

  function updateNavColors(_p2) {
    // The Pithora-Threshold navbar has its own painted palette
    // (terracotta wall + ivory paint) that should stay constant.
    // No scroll-linked color lerping — this is a deliberate no-op.
  }

  function updateNavbarState(scrollY) {
    navbar.classList.toggle('scrolled', scrollY > 50);
  }

  function handleScroll() {
    const scrollY   = window.scrollY;
    const maxScroll = document.documentElement.scrollHeight - window.innerHeight;
    const raw       = maxScroll > 0 ? Math.min(scrollY / maxScroll, 1) : 0;

    // Phase 1 (0–55%): particles organize — hero anchored
    // Phase 2 (50–100%): hero exits, background opens  — 5% overlap
    const p1 = clamp(raw / 0.55, 0, 1);
    const p2 = clamp((raw - 0.50) / 0.50, 0, 1);

    canvasProgress = p1;   // drives particle organization

    updateNavbarState(scrollY);
    updateHeroExit(p2);
    updateBackground(p2);
    updateNavColors(p2);
    updateHeroWordmark(scrollY);
  }

  // Fade the centered "Let's Think" wordmark as soon as the user scrolls.
  // Fully visible at 0, fully gone by ~40% of viewport height.
  function updateHeroWordmark(scrollY) {
    if (!heroWordmark) return;
    const fadeEnd = window.innerHeight * 0.40;
    const t       = clamp(scrollY / fadeEnd, 0, 1);
    const opacity = 1 - t;
    heroWordmark.style.opacity   = opacity.toFixed(3);
    // Gentle upward drift + subtle scale-down as it fades
    heroWordmark.style.transform =
      `translate(-50%, calc(-50% - ${(t * 18).toFixed(1)}px)) scale(${(1 - t * 0.04).toFixed(3)})`;
  }

  // ── Init ──────────────────────────────────────────────────
  document.addEventListener('DOMContentLoaded', function () {
    initCanvas();

    window.addEventListener('resize', () => {
      initCanvas();
      handleScroll();   // recalc scroll state after resize
    });

    window.addEventListener('scroll',    handleScroll, { passive: true });
    window.addEventListener('mousemove', e => { mouse.x = e.clientX; mouse.y = e.clientY; });
    window.addEventListener('mouseleave', () => { mouse.x = -9999; mouse.y = -9999; });

    handleScroll();   // apply initial state immediately
    tick();           // start animation loop
  });

})();

